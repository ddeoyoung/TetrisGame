CBlock::CBlock() : m_type(0), m_rot(0), m_x(0), m_y(0) {}

void CBlock::Init(int type)
{
    m_type = type;
    m_rot = 0; // 회전
    m_x = 3;
    m_y = -1;
}

void CBlock::RotateClockWise()  { m_rot = (m_rot + 1) % 4; }
void CBlock::RotateReverseClockWise() { m_rot = (m_rot + 3) % 4; }

void CBlock::GetCells(CellPos out[4])
{
    for (int i = 0; i < 4; i++) {
        out[i].row = s_blockData[m_type][m_rot][i].row + m_y;
        out[i].col = s_blockData[m_type][m_rot][i].col + m_x;
    }
}

void CBlock::GetLocalCells(CellPos out[4])
{
    for (int i = 0; i < 4; i++)
        out[i] = s_blockData[m_type][m_rot][i];
}
